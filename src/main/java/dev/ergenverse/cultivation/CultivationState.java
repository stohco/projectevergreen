    /**
     * Mark an Essence as comprehended by taxonomy order (1-14).
     * @param taxonomyOrder the essence taxonomy order (1=Metal, ..., 14=Reincarnation)
     */
    public void comprehendEssence(int taxonomyOrder) {
        if (taxonomyOrder >= 1 && taxonomyOrder <= 14) {
            essencesComprehended[taxonomyOrder - 1] = true;
        }
    }

    /**
     * Check if the Heaven-Defying Bead is in the player's inventory.
     * V1: checks for the registered ergenverse item by registry key path.
     */
    public boolean hasHeavenDefyingBead(net.minecraft.world.entity.player.Inventory inv) {
        for (int i = 0; i < inv.getContainerSize(); i++) {
            net.minecraft.world.item.ItemStack stack = inv.getItem(i);
            if (!stack.isEmpty()) {
                net.minecraft.resources.ResourceLocation rl = net.minecraftforge.registries.ForgeRegistries.ITEMS.getKey(stack.getItem());
                if (rl != null && rl.getPath().contains("heaven_defying_bead")) {
                    return true;
                }
            }
        }
        return false;
    }

