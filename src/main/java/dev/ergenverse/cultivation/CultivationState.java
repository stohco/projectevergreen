    /** The perception tier derived from the current realm. */
    public PerceptionTier getPerceptionTier() {
        return PerceptionTier.fromRealm(currentRealm);
    }

    /**
     * Compute the divine sense radius in blocks using the anti-lag formula.
     *
     * <p>Uses the player's actual S_sense (derived from realm baseline,
     * divine sense potency, and any manual bonuses) with the logarithmic
     * radius formula from {@link DivineSense#radius}.
     *
     * @param worldLawTier the world's law tier at the player's location
     * @return radius in blocks
     */
    public int getDivineSenseRadius(int worldLawTier) {
        if (currentRealm == RealmId.MORTAL) return 4; // mortals have no divine sense
        long sSense = computeSSense();
        return DivineSense.radius(sSense, worldLawTier, 16);
    }

    /** Compute S_sense from realm baseline × divine sense potency. */
    public long computeSSense() {
        long sRealm = DivineSense.S_REALM.getOrDefault(currentRealm, 1L);
        // divineSense is 0-1; at 1.0, S_sense = S_realm × 2 (full potency)
        // At 0.0, S_sense = S_realm × 0.5 (half from realm alone, minimal cultivation)
        return Math.round(sRealm * (0.5 + divineSense * 1.5));
    }

    // ─── Qi management ────────────────────────────────────────────────

    public double getQi() { return qi; }
    public double getMaxQi() { return maxQi; }

    /** Get the absolute Qi amount (qi fraction × maxQi). */
    public double getAbsoluteQi() { return qi * maxQi; }

    /**
     * Consume Qi. Returns the amount actually consumed (may be less if
     * insufficient Qi).
     */
    public double consumeQi(double amount) {
        double absoluteAmount = amount; // amount is in absolute Qi units
        double available = qi * maxQi;
        if (absoluteAmount > available) {
            double consumed = available;
            qi = 0.0;
            return consumed;
        }
        qi = (available - absoluteAmount) / maxQi;
        return absoluteAmount;
    }

    /**
     * Add Qi (from meditation, spirit stones, spirit veins, etc.).
     * Capped at 1.0.
     */
    public void addQi(double absoluteAmount) {
        double current = qi * maxQi;
        double newAmount = Math.min(current + absoluteAmount, maxQi);
        qi = newAmount / maxQi;
    }

