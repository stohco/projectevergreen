/**
 * The player's cultivation state — the complete record of their
 * progress along the Dao.
 *
 * <p><b>The Prime Directive:</b> reality is objective; cultivation changes
 * understanding, not existence. This state tracks what the cultivator
 * can perceive and interact with. It does not determine what exists in
 * the world.
 *
 * <h2>Design Principles</h2>
 * <ul>
 *   <li>Cultivation does NOT use XP. It uses events, comprehension, and
 *       tribulation.</li>
 *   <li>Breakthrough is EVENT-BASED. It requires: sufficient Qi +
 *       comprehension + tribulation survival.</li>
 *   <li>Every field carries meaning — no filler stats.</li>
 *   <li>Realm determines what you can perceive and interact with, NOT
 *       what exists.</li>
 * </ul>
 *
 * <h2>Fields and their meaning</h2>
 * <ul>
 *   <li><b>currentRealm</b> — position on the 18-step ladder. Determines
 *       perception tier, divine sense baseline, lifespan, and what
 *       breakthrough comes next.</li>
 *   <li><b>qi</b> — current Qi, normalized 0-1 against maxQi. This is
 *       spiritual energy in the meridians/dantian. Depleted by techniques,
 *       combat, breakthroughs. Regenerated by meditation, spirit stones,
 *       spirit veins.</li>
 *   <li><b>maxQi</b> — maximum Qi capacity. Grows with realm and blood
 *       refinement. A Qi Condensation cultivator holds far less than a
 *       Nascent Soul cultivator.</li>
 *   <li><b>divineSense</b> — normalized 0-1 measure of divine sense
 *       potency. Used with {@link DivineSense#soulPowerTotal} to compute
 *       actual S_sense. Rises through soul cultivation, soul-search
 *       methods, realm advancement.</li>
 *   <li><b>bloodRefinement</b> — body refinement progress 0-1. Affects
 *       maxQi, physical durability, and lifespan extension. In Renegade
 *       Immortal, Wang Lin's body cultivation was secondary but critical
 *       for survival at equal-realm encounters.</li>
 *   <li><b>karma</b> — karmic debt 0-1. Accumulates from killing,
 *       soul-searching, breaking formations, forbidden arts. High karma
 *       increases heart demon risk and can block breakthroughs entirely.
 *       In RI, karma was a real and dangerous force — not a mere number.</li>
 *   <li><b>breakthroughProgress</b> — 0-1 progress toward the NEXT
 *       breakthrough. This is NOT XP. It is a composite of events:
 *       meditation sessions, Dao insights, surviving tribulation stages,
 *       consuming rare herbs, receiving teachings. It represents
 *       readiness, not accumulation.</li>
 *   <li><b>tribulationPending</b> — whether Heaven has marked this
 *       cultivator for tribulation. Set to true when breakthroughProgress
 *       reaches 1.0 and all conditions are met. The tribulation must be
 *       faced before the breakthrough can complete.</li>
 *   <li><b>heartDemonRisk</b> — 0-1 risk of heart demon eruption during
 *       breakthrough. Computed from karma, past trauma, moral choices,
 *       and realm-specific factors. Nascent Soul and above have
 *       inherently higher risk.</li>
 *   <li><b>daoComprehension</b> — map of Dao path IDs to comprehension
 *       depth (0-1). A cultivator may understand multiple Daos but
 *       deeply comprehend only one or two. Breakthrough requires
 *       sufficient comprehension in at least one Dao.</li>
 *   <li><b>currentTechniques</b> — list of active technique IDs the
 *       cultivator has learned and can use.</li>
 *   <li><b>lifeForce</b> — remaining life force. Decreases with age
 *       (server tick time), can be extended by breakthroughs, certain
 *       herbs, and life-saving treasures. When it hits 0, the cultivator
 *       dies of old age — unless they find a way to extend their life.</li>
 *   <li><b>suppressionMultiplier</b> — 0-1 (default 1.0). Reduced by
 *       higher-realm cultivators' spiritual pressure. A suppressed
 *       cultivator operates at reduced capacity — Qi regeneration slows,
 *       techniques weaken, breakthrough becomes harder. In Er Gen,
 *       realm suppression was absolute: a Nascent Soul cultivator's
 *       pressure could prevent a Foundation cultivator from even
 *       standing.</li>
 * </ul>
 */
