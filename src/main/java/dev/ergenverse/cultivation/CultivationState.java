    /**
     * Summon a new Samsara Incarnation — a soul-fragment sent to live another life.
     *
     * <p>Canon: "One Billion Samsara Incarnations" (一亿分身). Each incarnation
     * lives a complete life in a different context. When reabsorbed, the
     * experiences return as Dao insight.
     *
     * <p>Requirements (v1): Heaven-Defying Bead in inventory (soul-vessel).
     * Cost: 10% of current absolute Qi (soul-fragment creation).
     *
     * @param inv the player's inventory (for Heaven-Defying Bead check)
     * @return the new incarnation, or null if not eligible
     */
    public dev.ergenverse.wanglin.SamsaraDao.SamsaraIncarnation summonIncarnation(
            net.minecraft.world.entity.player.Inventory inv) {
        if (!hasHeavenDefyingBead(inv)) return null;
        // Cost: 10% of current absolute Qi
        double cost = getAbsoluteQi() * 0.10;
        if (cost < 1.0) return null; // need at least some Qi to split off a fragment
        consumeQi(cost);

        String id = "inc_" + nextIncarnationId++;
        // Pick a context world from the canon options (v1: rotation among known worlds)
        String[] contexts = {
            "Mortal Town (lifespan 80 yrs)", "Suzaku Tomb (lifespan 200 yrs)",
            "Foreign Battleground (lifespan 50 yrs)", "Spirit Beast Forest (lifespan 300 yrs)",
            "Ancient God Cave (lifespan 500 yrs)", "Reincarnation Pool Cycle (lifespan 1000 yrs)",
            "Blood Ancestor Realm (lifespan 400 yrs)", "Ji Realm Wasteland (lifespan 120 yrs)"
        };
        String[] identities = {
            "wandering scholar", "village blacksmith", "wandering cultivator",
            "mountain hermit", "river fisherman", "scholar-warrior",
            "bandit chieftain", "temple keeper", "court advisor", "merchant's son"
        };
        java.util.Random rng = new java.util.Random();
        String context = contexts[rng.nextInt(contexts.length)];
        String identity = identities[rng.nextInt(identities.length)];
        int lifespan = 50 + rng.nextInt(950);

        var inc = new dev.ergenverse.wanglin.SamsaraDao.SamsaraIncarnation(
                id, context, identity, lifespan, false, null);
        samsaraIncarnations.add(inc);
        return inc;
    }

    /**
     * Reabsorb (merge) an active incarnation. Returns its Dao insight, which
     * can be converted into essence comprehension via {@link #comprehendEssence}.
     *
     * <p>Canon: when Wang Lin reabsorbs an incarnation, he gains its lifetime
     * of insight. v1: each merge grants one essence comprehension (the next
     * uncomprehended essence in Wang Lin's completion order).
     *
     * @param id the incarnation ID
     * @return the granted essence, or null if not found / already reabsorbed
     */
    public dev.ergenverse.wanglin.SamsaraDao.Essence mergeIncarnation(String id) {
        for (int i = 0; i < samsaraIncarnations.size(); i++) {
            var inc = samsaraIncarnations.get(i);
            if (inc.id.equals(id) && !inc.reabsorbed) {
                // Find next uncomprehended essence in Wang Lin's completion order
                dev.ergenverse.wanglin.SamsaraDao.Essence granted = null;
                for (var e : dev.ergenverse.wanglin.SamsaraDao.WANG_LIN_COMPLETION_ORDER) {
                    if (!essencesComprehended[e.taxonomyOrder - 1]) {
                        granted = e;
                        break;
                    }
                }
                String insightStr = granted != null
                        ? "Comprehended " + granted.name + " Essence"
                        : "All 14 Essences already comprehended; insight became universal Dao clarity.";
                // Replace with reabsorbed version
                var reabsorbed = new dev.ergenverse.wanglin.SamsaraDao.SamsaraIncarnation(
                        inc.id, inc.contextWorld, inc.incarnationIdentity,
                        inc.lifespanYears, true, insightStr);
                samsaraIncarnations.set(i, reabsorbed);

                if (granted != null) {
                    comprehendEssence(granted.taxonomyOrder);
                }
                return granted;
            }
        }
        return null;
    }

    /**
     * Find an incarnation by ID. Returns null if not found.
     */
    public dev.ergenverse.wanglin.SamsaraDao.SamsaraIncarnation getIncarnation(String id) {
        for (var inc : samsaraIncarnations) {
            if (inc.id.equals(id)) return inc;
        }
        return null;
    }

