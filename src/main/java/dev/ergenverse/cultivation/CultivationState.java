    // ─── Breakthrough logic ───────────────────────────────────────────

    /**
     * Check whether the cultivator CAN attempt a breakthrough.
     *
     * <p>This checks prerequisites only — it does not perform the
     * breakthrough. A successful attempt still requires surviving
     * tribulation and heart demons.
     *
     * <p>Conditions (per Renegade Immortal canon):
     * <ol>
     *   <li>Not already at maximum realm (Transcendence).</li>
     *   <li>Qi ≥ 80% of maxQi (need energy for the transformation).</li>
     *   <li>Breakthrough progress ≥ 1.0 (events have prepared the way).</li>
     *   <li>Karma &lt; 0.90 (extreme karma blocks the path outright).</li>
     *   <li>At least one Dao path with sufficient comprehension for the
     *       target realm.</li>
     * </ol>
     *
     * @return true if all prerequisites are met
     */
    public boolean canBreakthrough() {
        RealmId next = currentRealm.next();
        if (next == null) return false;            // already at Transcendence
        if (qi < QI_THRESHOLD) return false;        // not enough Qi
        if (breakthroughProgress < 1.0) return false; // not enough preparation
        if (karma >= KARMA_HARD_BLOCK) return false;  // karma blocks the path
        if (getHighestComprehension() < requiredComprehension()) return false; // no Dao deep enough
        return true;
    }

    /**
     * Compute the comprehension threshold for the next realm.
     *
     * <p>Higher realms require deeper Dao understanding. The base
     * threshold is 0.30, multiplied by a realm-specific factor that
     * increases with each step.
     */
    public double requiredComprehension() {
        double multiplier = REALM_COMPREHENSION_MULTIPLIER.getOrDefault(currentRealm, 1.0);
        return Math.min(1.0, COMPREHENSION_THRESHOLDS_BASE * multiplier);
    }

    /**
     * Attempt a breakthrough to the next realm.
     *
     * <p>This method checks all prerequisites and evaluates the three
     * breakthrough risks in order:
     * <ol>
     *   <li><b>Karma check</b> — extreme karma blocks outright.</li>
     *   <li><b>Comprehension check</b> — insufficient comprehension fails.</li>
     *   <li><b>Heart demon roll</b> — based on karma + realm-specific factors.</li>
     *   <li><b>Tribulation</b> — flagged as pending. The actual tribulation
     *       is handled by {@link CultivationEvents} (lightning strikes,
     *       player damage). If the player survives, the breakthrough
     *       completes.</li>
     * </ol>
     *
     * <p>If all checks pass, tribulation is set to pending and the
     * caller must trigger the actual tribulation event.
     *
     * @param rng random source for heart demon / tribulation rolls
     * @return the breakthrough result
     */
    public BreakthroughResult attemptBreakthrough(java.util.Random rng) {
        RealmId next = currentRealm.next();
        if (next == null) {
            return new BreakthroughResult(
                    BreakthroughResult.Outcome.INSUFFICIENT_COMPREHENSION,
                    currentRealm, currentRealm,
                    "Already at the peak of Transcendence. There is no higher realm.");
        }

        // 1. Karma hard block
        if (karma >= KARMA_HARD_BLOCK) {
            return BreakthroughResult.karmaBlocked(currentRealm,
                    "Karmic debt weighs upon the heavenly path like a mountain. " +
                    "The tribulation would be ten times stronger. Settle your debts first.");
        }

        // 2. Comprehension check
        double reqComp = requiredComprehension();
        double highestComp = getHighestComprehension();
        if (highestComp < reqComp) {
            return BreakthroughResult.insufficientComprehension(currentRealm,
                    String.format("Dao comprehension (%.2f) below the threshold for %s (%.2f). " +
                    "Meditate more deeply on your Dao.",
                    highestComp, next.name, reqComp));
        }

        // 3. Qi check
        if (qi < QI_THRESHOLD) {
            return BreakthroughResult.failedTribulation(currentRealm,
                    String.format("Qi reserves (%.1f%%) insufficient for breakthrough. " +
                    "Need at least %.0f%%. Meditate near a spirit vein or consume spirit stones.",
                    qi * 100, QI_THRESHOLD * 100));
        }

        // 4. Heart demon risk
        double effectiveHeartDemonRisk = computeHeartDemonRisk();
        if (rng.nextDouble() < effectiveHeartDemonRisk) {
            // Heart demon erupts! Breakthrough fails. Karma increases.
            this.karma = clamp01(karma + 0.05);
            this.heartDemonRisk = clamp01(heartDemonRisk + 0.1);
            this.qi = Math.max(0, qi - 0.3); // significant Qi loss
            this.breakthroughProgress = Math.max(0, breakthroughProgress - 0.2);
            return BreakthroughResult.heartDemon(currentRealm,
                    String.format("At the critical moment, a heart demon erupted! " +
                    "Your karma (%.2f) fed the demon. Qi depleted, progress lost. " +
                    "Resolve your inner conflicts before trying again.", karma));
        }

        // 5. All checks passed — tribulation is now pending
        this.tribulationPending = true;
        return new BreakthroughResult(
                BreakthroughResult.Outcome.SUCCESS,
                currentRealm, next,
                "The heavens sense your audacity. Tribulation descends!");
        // NOTE: The actual realm change happens after tribulation survival,
        // handled by CultivationEvents.completeBreakthrough()
    }

    /**
     * Complete the breakthrough after tribulation survival.
     *
     * <p>Called by {@link CultivationEvents} after the player endures
     * the tribulation lightning. This actually changes the realm.
     *
     * @return the new realm, or null if no breakthrough was pending
     */
    public RealmId completeBreakthrough() {
        if (!tribulationPending) return null;
        RealmId next = currentRealm.next();
        if (next == null) return null;

        // Consume Qi for the transformation
        this.qi = 0.1; // left with 10% after the massive expenditure

        // Advance the realm
        setCurrentRealm(next);

        // Karma is partially cleansed by tribulation (Heaven's test burns karma)
        this.karma = clamp01(karma * 0.7);

        // Heart demon risk decreases with successful breakthrough
        this.heartDemonRisk = clamp01(heartDemonRisk * 0.5);

        // Divine sense grows with realm
        this.divineSense = clamp01(Math.max(divineSense, 0.3 + currentRealm.order * 0.04));

        this.tribulationPending = false;
        return next;
    }

    /**
     * Fail the breakthrough after tribulation kills the cultivator.
     *
     * <p>Qi is severely depleted, karma increases, progress is lost.
     * The cultivator survives but is weakened.
     */
    public void failBreakthrough() {
        this.qi = Math.max(0, qi - 0.5);
        this.karma = clamp01(karma + 0.03);
        this.breakthroughProgress = Math.max(0, breakthroughProgress - 0.3);
        this.heartDemonRisk = clamp01(heartDemonRisk + 0.05);
        this.tribulationPending = false;
    }

    /**
     * Compute the effective heart demon risk for the current breakthrough attempt.
     *
     * <p>Factors (per Renegade Immortal canon):
     * <ul>
     *   <li>Base karma contribution: karma ≥ 0.50 adds 0.1-0.4 risk</li>
     *   <li>Realm contribution: Nascent Soul+ adds inherent risk</li>
     *   <li>Existing heartDemonRisk: accumulates across failed attempts</li>
     * </ul>
     */
    private double computeHeartDemonRisk() {
        double risk = heartDemonRisk * 0.5; // persistent risk from past failures

        // Karma contribution
        if (karma >= KARMA_HEART_DEMON_FLOOR) {
            double karmaFactor = (karma - KARMA_HEART_DEMON_FLOOR) / (1.0 - KARMA_HEART_DEMON_FLOOR);
            risk += karmaFactor * 0.4;
        }

        // Realm contribution: higher realms have inherently more heart demon risk
        // Nascent Soul (order 4) and above
        if (currentRealm.order >= 4) {
            risk += 0.05 + (currentRealm.order - 4) * 0.02;
        }

        return Math.min(0.95, risk);
    }

    // ─── Internal calculations ────────────────────────────────────────

    /** Recalculate max Qi based on realm and blood refinement. */
    private void recalculateMaxQi() {
        double base = REALM_MAX_QI_BASE.getOrDefault(currentRealm, 10.0);
        // Blood refinement adds up to 50% more max Qi
        this.maxQi = base * (1.0 + bloodRefinement * 0.5);
        // Ensure qi doesn't exceed new max (fractional)
        this.qi = Math.min(qi, 1.0);
    }

    /** Recalculate life force ceiling based on current realm's lifespan. */
    private void recalculateLifeForce() {
        // If lifeForce was at or above the old realm's lifespan, raise it
        // to the new realm's lifespan (breakthrough extends life).
        this.lifeForce = currentRealm.lifespan;
    }

    // ─── Tick-based updates (called from CultivationEvents) ──────────

    /**
     * Per-tick update. Handles passive Qi regeneration and life force decay.
     *
     * <p>Qi regeneration rate depends on realm and suppression:
     * <ul>
     *   <li>Qi Condensation: very slow natural regen (0.0001/tick)</li>
     *   <li>Foundation: 0.0003/tick</li>
     *   <li>Higher realms: faster, but still meaningful</li>
     *   <li>Suppression reduces regen rate proportionally</li>
     * </ul>
     *
     * <p>Life force decays by 1 tick of the realm's lifespan per tick.
     * For a mortal (100-year lifespan = ~52,560,000 ticks), this is
     * negligible per tick but meaningful over time.
     */
    public void serverTick() {
        tickCounter++;

        // Qi regeneration (every 20 ticks = 1 second)
        if (tickCounter % 20 == 0 && qi < 1.0 && currentRealm != RealmId.MORTAL) {
            double baseRate = getQiRegenRatePerSecond();
            double effectiveRate = baseRate * suppressionMultiplier;
            addQiAbsoluteNormalized(effectiveRate);
        }

        // Life force decay (every 600 ticks = 30 seconds for efficiency)
        if (tickCounter % 600 == 0) {
            // Lose 30 seconds of life per 30-second check
            lifeForce = Math.max(0, lifeForce - 30.0);
        }
    }

    /**
     * Get the Qi regeneration rate per second as a fraction of maxQi.
     *
     * <p>In Er Gen, Qi regeneration is slow and deliberate. It's not a
     * rapid cooldown. Cultivators meditate for hours or days.
     */
    private double getQiRegenRatePerSecond() {
        return switch (currentRealm) {
            case MORTAL -> 0.0;             // mortals have no Qi to regenerate
            case QI_CONDENSATION -> 0.0001; // ~16 minutes to fully regen
            case FOUNDATION -> 0.0003;      // ~5.5 minutes
            case CORE_FORMATION -> 0.0005;  // ~3.3 minutes
            case NASCENT_SOUL -> 0.001;     // ~1.7 minutes
            case SOUL_FORMATION -> 0.002;   // ~50 seconds
            case SOUL_TRANSFORMATION -> 0.003; // ~33 seconds
            case ASCENDANT -> 0.005;        // ~20 seconds
            default -> 0.008;               // higher realms: ~12 seconds
        };
    }

    /** Add Qi as a fraction of maxQi. */
    private void addQiAbsoluteNormalized(double fraction) {
        qi = Math.min(1.0, qi + fraction);
    }

    // ─── NBT serialization ────────────────────────────────────────────

    @Override
    public CompoundTag serializeNBT() {
        CompoundTag tag = new CompoundTag();
        tag.putInt(TAG_REALM, currentRealm.order);
        tag.putDouble(TAG_QI, qi);
        tag.putDouble(TAG_MAX_QI, maxQi);
        tag.putDouble(TAG_DIVINE_SENSE, divineSense);
        tag.putDouble(TAG_BLOOD_REFINEMENT, bloodRefinement);
        tag.putDouble(TAG_KARMA, karma);
        tag.putDouble(TAG_BREAKTHROUGH_PROGRESS, breakthroughProgress);
        tag.putBoolean(TAG_TRIBULATION_PENDING, tribulationPending);
        tag.putDouble(TAG_HEART_DEMON_RISK, heartDemonRisk);
        tag.putDouble(TAG_LIFE_FORCE, lifeForce);
        tag.putDouble(TAG_SUPPRESSION, suppressionMultiplier);

        // Dao comprehension: each entry as "daoId:comprehension"
        CompoundTag daoTag = new CompoundTag();
        for (Map.Entry<String, Double> entry : daoComprehension.entrySet()) {
            daoTag.putDouble(entry.getKey(), entry.getValue());
        }
        tag.put(TAG_DAO_COMPREHENSION, daoTag);

        // Techniques: list of strings
        ListTag techList = new ListTag();
        for (String tech : currentTechniques) {
            techList.add(StringTag.valueOf(tech));
        }
        tag.put(TAG_CURRENT_TECHNIQUES, techList);

        // Essences: byte array (14 booleans)
        byte[] essenceBytes = new byte[14];
        for (int i = 0; i < 14; i++) essenceBytes[i] = (byte) (essencesComprehended[i] ? 1 : 0);
        tag.putByteArray(TAG_ESSENCES, essenceBytes);

        // Samsara Incarnations: list of compounds
        ListTag incList = new ListTag();
        for (dev.ergenverse.wanglin.SamsaraDao.SamsaraIncarnation inc : samsaraIncarnations) {
            CompoundTag incTag = new CompoundTag();
            incTag.putString("id", inc.id);
            incTag.putString("context_world", inc.contextWorld);
            incTag.putString("identity", inc.incarnationIdentity);
            incTag.putInt("lifespan_years", inc.lifespanYears);
            incTag.putBoolean("reabsorbed", inc.reabsorbed);
            if (inc.daoInsightGained != null) {
                incTag.putString("dao_insight", inc.daoInsightGained);
            }
            incList.add(incTag);
        }
        tag.put(TAG_SAMSARA_INCARNATIONS, incList);
        tag.putInt(TAG_NEXT_INCARNATION_ID, nextIncarnationId);

        return tag;
    }

    @Override
    public void deserializeNBT(CompoundTag tag) {
        this.currentRealm = RealmId.byOrder(tag.getInt(TAG_REALM));
        this.qi = tag.getDouble(TAG_QI);
        this.maxQi = tag.getDouble(TAG_MAX_QI);
        this.divineSense = tag.getDouble(TAG_DIVINE_SENSE);
        this.bloodRefinement = tag.getDouble(TAG_BLOOD_REFINEMENT);
        this.karma = tag.getDouble(TAG_KARMA);
        this.breakthroughProgress = tag.getDouble(TAG_BREAKTHROUGH_PROGRESS);
        this.tribulationPending = tag.getBoolean(TAG_TRIBULATION_PENDING);
        this.heartDemonRisk = tag.getDouble(TAG_HEART_DEMON_RISK);
        this.lifeForce = tag.getDouble(TAG_LIFE_FORCE);
        this.suppressionMultiplier = tag.getDouble(TAG_SUPPRESSION);

        // Dao comprehension
        this.daoComprehension.clear();
        if (tag.contains(TAG_DAO_COMPREHENSION, Tag.TAG_COMPOUND)) {
            CompoundTag daoTag = tag.getCompound(TAG_DAO_COMPREHENSION);
            for (String key : daoTag.getAllKeys()) {
                this.daoComprehension.put(key, daoTag.getDouble(key));
            }
        }

        // Techniques
        this.currentTechniques.clear();
        if (tag.contains(TAG_CURRENT_TECHNIQUES, Tag.TAG_LIST)) {
            ListTag techList = tag.getList(TAG_CURRENT_TECHNIQUES, Tag.TAG_STRING);
            for (int i = 0; i < techList.size(); i++) {
                this.currentTechniques.add(techList.getString(i));
            }
        }

        // Essences
        if (tag.contains(TAG_ESSENCES, Tag.TAG_BYTE_ARRAY)) {
            byte[] eb = tag.getByteArray(TAG_ESSENCES);
            for (int i = 0; i < Math.min(eb.length, 14); i++) {
                this.essencesComprehended[i] = eb[i] != 0;
            }
        }

        // Samsara Incarnations
        this.samsaraIncarnations.clear();
        if (tag.contains(TAG_SAMSARA_INCARNATIONS, Tag.TAG_LIST)) {
            ListTag incList = tag.getList(TAG_SAMSARA_INCARNATIONS, Tag.TAG_COMPOUND);
            for (int i = 0; i < incList.size(); i++) {
                CompoundTag incTag = incList.getCompound(i);
                String daoInsight = incTag.contains("dao_insight") ? incTag.getString("dao_insight") : null;
                this.samsaraIncarnations.add(new dev.ergenverse.wanglin.SamsaraDao.SamsaraIncarnation(
                        incTag.getString("id"),
                        incTag.getString("context_world"),
                        incTag.getString("identity"),
                        incTag.getInt("lifespan_years"),
                        incTag.getBoolean("reabsorbed"),
                        daoInsight
                ));
            }
        }
        this.nextIncarnationId = tag.getInt(TAG_NEXT_INCARNATION_ID);
        if (this.nextIncarnationId < 1) this.nextIncarnationId = 1;

        // Sanity: recalculate derived values
        recalculateMaxQi();
    }

