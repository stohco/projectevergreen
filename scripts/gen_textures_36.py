# SEA SERPENT (128x64) — 8-segment UV layout
# ═══════════════════════════════════════════════════════════════════
def gen_sea_serpent():
    img = Image.new("RGBA", (128, 64), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    
    scale_color = (40, 80, 60)      # deep green scales
    belly_color = (80, 120, 90)     # lighter belly
    ridge_color = (30, 60, 40)      # dorsal fin dark
    whisker_color = (100, 100, 110) # gray barbels
    eye_color = (200, 180, 50)      # amber eye
    jaw_color = (50, 70, 55)       # darker jaw
    
    # 8 body segments: each 8px tall, spanning texOffs(0, 0→56)
    for seg_i in range(8):
        y = seg_i * 8
        # Width tapers: wider at front (seg 0), narrower at rear (seg 7)
        w = 12 - seg_i  # tex width in pixels
        x_off = 0
        # Shading: top is lighter (dorsal), bottom is darker (ventral shading is inverted for fish — belly lighter)
        top = lighten(scale_color, 15 + seg_i * 2)
        bot = darken(scale_color, 0.8 + seg_i * 0.02)
        draw_gradient_rect(draw, x_off, y, w, 8, top, belly_color if seg_i < 4 else bot)
        # Scale pattern — overlapping arcs
        for row in range(2):
            for col in range(w - 1):
                sx = x_off + col
                sy = y + 1 + row * 3
                if (col + row) % 2 == 0:
                    draw.arc([sx, sy, sx+3, sy+3], 0, 180, fill=darken(scale_color, 0.6), width=1)
        # Lateral line on even segments (2, 4, 6)
        if seg_i in [2, 4, 6]:
            draw.line([(x_off + 1, y + 3), (x_off + w - 2, y + 3)], fill=darken(scale_color, 0.4), width=1)
    
    # Neck (16,0) — 2.4x2.4x2.5
    draw_gradient_rect(draw, 16, 0, 6, 6, lighten(scale_color, 10), belly_color)
    
    # Head (0,48) — 3.6x2.0x2.5 broad flat skull
    draw_gradient_rect(draw, 0, 48, 10, 6, lighten(scale_color, 20), scale_color)
    # Scale texture on head
    for i in range(3):
        draw.line([(1, 49 + i*2), (8, 49 + i*2)], fill=darken(scale_color, 0.7))
    # Nostril
    draw.rectangle([4, 48, 5, 48], fill=darken(scale_color, 0.4))
    
    # Jaw (16,48) — 3.0x0.8x2.2
    draw.rectangle([16, 48, 22, 52], fill=jaw_color)
    # Teeth hints
    for i in range(4):
        draw.point((17 + i, 48), fill=(220, 220, 210))
    
    # Eyes (24,48) — amber, positioned on top of skull
    draw.rectangle([24, 48, 25, 49], fill=eye_color)
    draw.rectangle([24, 50, 25, 51], fill=darken(eye_color, 0.5))
    draw.point((24, 48), fill=(255, 255, 200))  # eye shine
    
    # Whiskers (28,48) — 0.2x0.2x4.0 long barbels
    draw.line([(28, 49), (31, 52)], fill=whisker_color, width=1)
    draw.line([(28, 52), (31, 55)], fill=darken(whisker_color, 0.8), width=1)
    
    # Dorsal fins on segments 1, 3, 5 (32, i*6)
    for fi, seg in enumerate([1, 3, 5]):
        fh = 8 - fi * 2  # fin height decreases toward tail
        y = fi * 6
        draw.rectangle([32, y, 33, y + fh], fill=ridge_color)
        # Fin membrane rays
        for r in range(fh):
            alpha = 0.3 + 0.3 * math.sin(r * 0.5)
            draw.line([(32, y + r), (33, y + r)], fill=lighten(ridge_color, int(alpha * 40)))
    
    # Lateral line ridges on segments 2, 4, 6 (48, i*4)
    for li, seg in enumerate([2, 4, 6]):
        y = li * 4
        draw.rectangle([48, y, 50, y + 2], fill=darken(scale_color, 0.5))
    
    # Pectoral fins (56, 0→8)
    draw.rectangle([56, 0, 62, 2], fill=lighten(fin_color if 'fin_color' in dir() else scale_color, 10))
    draw.rectangle([56, 4, 62, 6], fill=darken(scale_color, 0.7))
    
    # Tail fin (16, 12) — wide vertical fin
    draw.rectangle([16, 12, 22, 20], fill=lighten(scale_color, 15))
    # Fin rays
    for r in range(8):
        draw.line([(16, 12 + r), (22, 12 + r)], fill=darken(scale_color, 0.5 + 0.05 * r))
    
    add_noise(img, 6)
    img.save(os.path.join(TEX_DIR, "sea_serpent.png"))
    return "sea_serpent.png (128x64, 8-segment UV)"

