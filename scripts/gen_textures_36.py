# SOUL FISH (64x32) — pale blue-silver with qi glow
# ═══════════════════════════════════════════════════════════════════
def gen_soul_fish():
    img = Image.new("RGBA", (64, 32), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    
    body_color = (140, 180, 220)    # pale blue-silver
    belly_color = (200, 220, 240)  # lighter belly
    fin_color = (100, 150, 200)    # slightly darker fins
    eye_color = (30, 80, 180)      # deep blue eye
    qi_color = (180, 200, 255)     # qi glow hint
    
    # Body (0,0 → 12,8) - 3.0x2.0x4.0 at texOffs(0,0)
    draw_gradient_rect(draw, 0, 0, 12, 8, lighten(body_color, 20), darken(body_color, 0.8))
    # Belly highlight on bottom rows
    draw.rectangle([1, 5, 11, 7], fill=belly_color)
    # Scale pattern (horizontal lines)
    for i in range(3):
        yy = 1 + i * 2
        draw.line([(1, yy), (10, yy)], fill=darken(body_color, 0.6), width=1)
    
    # Head (12,0 → 20,4) - 2.0x1.6x1.5 at texOffs(12,0)
    draw_gradient_rect(draw, 12, 0, 8, 6, lighten(body_color, 30), body_color)
    # Eyes on sides
    draw.rectangle([13, 1, 14, 2], fill=eye_color)
    draw.rectangle([17, 1, 18, 2], fill=eye_color)
    # Eye shine
    draw.point((13, 1), fill=(200, 220, 255))
    draw.point((17, 1), fill=(200, 220, 255))
    
    # Mouth (22,0 → 24,1) at texOffs(22,0)
    draw.rectangle([22, 0, 24, 1], fill=darken(body_color, 0.5))
    
    # Dorsal fin (0,8 → 2,14) at texOffs(0,8) - tall thin membrane
    draw.rectangle([0, 8, 1, 14], fill=lighten(fin_color, 20))
    # Fin rays
    for i in range(3):
        draw.line([(0, 9 + i*2), (1, 9 + i*2)], fill=darken(fin_color, 0.7))
    
    # Anal fin (0,12 → 2,16) at texOffs(0,12)
    draw.rectangle([0, 12, 1, 15], fill=fin_color)
    
    # Pectoral fins (4,8 → 10,10) at texOffs(4,8)
    draw.rectangle([4, 8, 10, 9], fill=fin_color)
    draw.rectangle([4, 10, 10, 11], fill=fin_color)
    
    # Tail root (12,4 → 16,8) at texOffs(12,4)
    draw_gradient_rect(draw, 12, 4, 4, 4, body_color, darken(body_color, 0.7))
    
    # Tail fan (0,14 → 6,18) at texOffs(0,14) - wide caudal fin
    draw.rectangle([0, 14, 6, 15], fill=lighten(fin_color, 10))  # top lobe
    draw.rectangle([0, 16, 6, 17], fill=darken(fin_color, 0.8))   # bottom lobe
    # Fan rays
    for i in range(7):
        draw.line([(i, 14), (i, 17)], fill=darken(fin_color, 0.5))
    
    # Qi glow aura (0,20 → 8,28) at texOffs(0,20) - transparent blue
    for y in range(20, 28):
        for x in range(8):
            dx = abs(x - 4) / 4.0
            dy = abs(y - 24) / 4.0
            d = math.sqrt(dx*dx + dy*dy)
            if d < 1.0:
                alpha = int(40 * (1.0 - d))
                px = img.getpixel((x, y))
                r = min(255, px[0] + int(qi_color[0] * alpha / 255))
                g = min(255, px[1] + int(qi_color[1] * alpha / 255))
                b = min(255, px[2] + int(qi_color[2] * alpha / 255))
                img.putpixel((x, y), (r, g, b, max(px[3], alpha)))
    
    # Lateral line (24,0 → 28,6) at texOffs(24,0)
    draw.line([(24, 1), (27, 5)], fill=darken(body_color, 0.4), width=1)
    
    add_noise(img, 5)
    img.save(os.path.join(TEX_DIR, "soul_fish.png"))
    return "soul_fish.png"

