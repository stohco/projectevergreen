def darken(color, factor=0.7):
    return tuple(max(0, int(c * factor)) for c in color)

def lighten(color, amount=30):
    return tuple(min(255, c + amount) for c in color)

def lerp_color(c1, c2, t):
    return tuple(int(c1[i] + (c2[i] - c1[i]) * t) for i in range(3))

def add_noise(img, amount=8):
    pixels = img.load()
    w, h = img.size
    for y in range(h):
        for x in range(w):
            r, g, b = pixels[x, y][:3]
            n = random.randint(-amount, amount)
            pixels[x, y] = (max(0,min(255,r+n)), max(0,min(255,g+n)), max(0,min(255,b+n)))

def draw_gradient_rect(draw, x, y, w, h, color_top, color_bot):
    """Draw a rectangle with vertical gradient (top lighter, bottom darker)."""
    for row in range(h):
        t = row / max(h - 1, 1)
        c = lerp_color(color_top, color_bot, t)
        draw.line([(x, y + row), (x + w - 1, y + row)], fill=c)

