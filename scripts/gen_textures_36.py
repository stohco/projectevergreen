def gen_qilin():
    """Qilin: golden-scaled body, iridescent blue-green mane, branching antlers."""
    img = Image.new("RGBA", (64, 64), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    
    scale_gold = (200, 170, 80)    # golden scales
    scale_dark = (150, 120, 50)    # darker scale shadow
    scale_light = (230, 210, 120)  # scale highlight
    mane = (60, 140, 160)         # iridescent blue-green mane
    mane_dark = (40, 100, 120)     # darker mane
    antler = (180, 160, 100)       # tan antlers
    wing = (180, 190, 210)         # light wing membrane
    eye = (100, 60, 30)            # deep brown eye
    
    # Body (golden scaled)
    draw_gradient_rect(draw, 0, 0, 12, 16, scale_light, scale_dark)
    # Scale pattern (overlapping arcs)
    for row in range(4):
        for col in range(3):
            sx = 1 + col * 4
            sy = 1 + row * 4
            draw.arc([sx, sy, sx+4, sy+4], 0, 180, fill=scale_dark, width=1)
    
    # Neck
    draw_gradient_rect(draw, 12, 0, 8, 8, scale_gold, scale_dark)
    
    # Head
    draw.rectangle([20, 0, 28, 8], fill=lighten(scale_gold, 10))
    draw.rectangle([22, 2, 23, 3], fill=eye)
    draw.point((22, 2), fill=(220, 200, 150))
    # Jaw
    draw.rectangle([28, 0, 32, 4], fill=darken(scale_gold, 0.7))
    
    # Antlers (branched)
    draw.rectangle([32, 0, 34, 8], fill=antler)
    draw.rectangle([34, 2, 36, 5], fill=antler)  # branch 1
    draw.rectangle([36, 0, 38, 3], fill=darken(antler, 0.8))  # tine 1
    draw.rectangle([34, 5, 37, 7], fill=darken(antler, 0.9))  # branch 2
    
    # Mane (5 segments of iridescent blue-green)
    for i in range(5):
        y = 16 + i * 4
        draw.rectangle([0, y, 8, y + 3], fill=mane)
        draw.rectangle([2, y + 1, 6, y + 2], fill=mane_dark)
    
    # Wings (small, tucked)
    draw.rectangle([0, 36, 12, 40], fill=wing)
    draw.line([(2, 37), (10, 37)], fill=darken(wing, 0.7))
    draw.line([(2, 39), (10, 39)], fill=darken(wing, 0.7))
    
    # Scale plates on flanks
    draw.rectangle([16, 16, 20, 20], fill=lighten(scale_gold, 5))
    draw.rectangle([16, 20, 20, 24], fill=scale_dark)
    
    # Tail (tufted)
    for i in range(3):
        draw.rectangle([24, 24 + i*4, 28, 28 + i*4], fill=lerp_color(mane, scale_gold, i/2.0))
    
    # Legs
    for ly in [40, 48]:
        for lx in [0, 8]:
            draw.rectangle([lx, ly, lx + 4, ly + 6], fill=scale_dark)
            draw.rectangle([lx, ly + 5, lx + 3, ly + 6], fill=darken(scale_dark, 0.7))
    
    add_noise(img, 7)
    img.save(os.path.join(TEX_DIR, "qilin.png"))
    return "qilin.png"

